a = input()
ans1, ans2, ans3, ans4, ans5, ans6 =0,0,0,0,0,0
for c in a:
	if c=='1': ans1 += 1
	if c=='2': ans2 += 1
	if c=='3': ans3 += 1
	if c=='4': ans4 += 1
	if c=='5': ans5 += 1
	if c=='6': ans6 += 1
print('1:', ans1, sep='')
print('2:', ans2, sep='')
print('3:', ans3, sep='')
print('4:', ans4, sep='')
print('5:', ans5, sep='')
print('6:', ans6, sep='')